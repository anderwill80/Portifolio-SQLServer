						WHERE
						PRPSIT = ''P'' AND 
						PRPMOVEST = ''S''AND
						ESTLOC IN (1,2)
						GROUP BY 
						PRPCLICOD,PRPNUM,A.PROCOD,C.VENNOM,F.PRODES,F.PROUM1,PRPQTD,PRPQTDEMB,PRPPRELIQ,PDVUSUGER,PDVDATCAD,PRPSIT,PRPESTLOC') 
	
