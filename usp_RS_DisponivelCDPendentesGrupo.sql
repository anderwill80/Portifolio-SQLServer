-- CRIANDO SALDO DISPONIVEL DO CD
if object_id('TempDB.dbo.#TBS032CD') is not null
   drop table #TBS032CD
	
SELECT PROCOD, ESTQTDATU - ESTQTDRES AS DISPONCD 
INTO #TBS032CD
FROM 
cd.SIBD.dbo.TBS032 
WHERE  
ESTLOC = 1 AND (ESTQTDATU - ESTQTDRES) <> 0

-- CRIANDO INDICE
if object_id('TempDB.dbo.#p') is not null
   drop table #p

SELECT 
rank() OVER (ORDER BY PENCOD,PENDAT,PENNUM) AS PENIDE,A.*,
ISNULL(DISPONCD,0) AS DISPONCD 
INTO #p
FROM #pengeral A
LEFT JOIN #TBS032CD B ON A.PENCOD collate database_default = B.PROCOD

-- ACUMULANDO PEDIDOS
if object_id('TempDB.dbo.#pen') is not null
	drop table #pen

SELECT *,
PENDIS - (SELECT SUM(PENQTDITE) FROM #p AS TI WHERE TI.PENIDE <= TA.PENIDE AND TI.PENCOD = TA.PENCOD AND TI.PENEMP = TA.PENEMP ) AS PENDISACU,
PENCMP - (SELECT SUM(PENQTDITE) FROM #p AS TI WHERE TI.PENIDE <= TA.PENIDE AND TI.PENCOD = TA.PENCOD AND TI.PENEMP = TA.PENEMP ) AS PENCMPACU,
DISPONCD - (SELECT SUM(PENQTDITE) FROM #p AS TI WHERE TI.PENIDE <= TA.PENIDE AND TI.PENCOD = TA.PENCOD ) AS PENACU,

CASE WHEN PENDIS - (SELECT SUM(PENQTDITE) FROM #p AS TI WHERE TI.PENIDE <= TA.PENIDE AND TI.PENCOD = TA.PENCOD AND TI.PENEMP = TA.PENEMP ) >=0
	THEN 'VERIFICAR SALDO'
	ELSE
	CASE WHEN PENCMP - (SELECT SUM(PENQTDITE) FROM #p AS TI WHERE TI.PENIDE <= TA.PENIDE AND TI.PENCOD = TA.PENCOD AND TI.PENEMP = TA.PENEMP ) >= 0 		  
		THEN 'ESTÁ EM COMPRAS'
		ELSE
		''
		END
	END AS OBS,

CASE WHEN DISPONCD = 0
	Then 'NAO ATENDE'
	Else
		CASE WHEN DISPONCD >= (SELECT SUM(PENQTDITE) FROM #p AS TI WHERE TI.PENIDE <= TA.PENIDE AND TI.PENCOD = TA.PENCOD )
			THEN 'TOTAL'
			ELSE 'PARCIAL'
			END
	End 
	AS SITUACAO

INTO #pen
FROM #p	AS TA



