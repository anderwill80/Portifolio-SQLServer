-- ND	

SELECT
	'ND' collate Latin1_General_BIN AS 'ND',
	'PDV' collate Latin1_General_BIN AS 'PDV',
	C.VENNOM collate Latin1_General_BIN  AS VEN,
	PRPCLICOD AS CLICOD,
	PRPNUM AS PED , 
	A.PROCOD collate Latin1_General_BIN AS COD,
	F.PRODES collate Latin1_General_BIN AS DESCRICAO,
	F.PROUM1 collate Latin1_General_BIN AS UNI1,
	(PRPQTD*PRPQTDEMB) AS QTD ,
	(PRPPRELIQ/PRPQTDEMB)*(PRPQTD * PRPQTDEMB) as VAL,
	CASE WHEN SUBSTRING(PDVUSUGER,1,19) <> '' 
		THEN SUBSTRING(PDVUSUGER,1,19)
		ELSE PDVDATCAD END AS DATCAD ,
	DATEDIFF(DAY,PDVDATCAD,getdate()) AS DIAS_PEN,
	PRPSIT collate Latin1_General_BIN AS SIT,
	PRPESTLOC AS ESTLOC,
	(SELECT SUM(ESTQTDCMP)FROM TBS032 J(NOLOCK) WHERE A.PROCOD = J.PROCOD AND ESTLOC IN(1,2) GROUP BY PROCOD) AS COMPRAS,
	(SELECT SUM(ESTQTDATU-ESTQTDRES)FROM TBS032 J(NOLOCK) WHERE A.PROCOD = J.PROCOD AND ESTLOC IN(1,2) GROUP BY PROCOD) AS DISPONIVEL,
	(SELECT SUM(ESTQTDATU-ESTQTDRES)FROM TBS032 J(NOLOCK) WHERE A.PROCOD = J.PROCOD AND ESTLOC IN(1) GROUP BY PROCOD) AS EST,
	(SELECT SUM(ESTQTDATU-ESTQTDRES)FROM TBS032 J(NOLOCK) WHERE A.PROCOD = J.PROCOD AND ESTLOC IN(2) GROUP BY PROCOD) AS LOJA
	
	FROM TBS058 A (NOLOCK) 
	LEFT JOIN TBS055 B (NOLOCK) ON A.PRPNUM = B.PDVNUM
	LEFT JOIN TBS010 F (NOLOCK) ON A.PROCOD = F.PROCOD
	LEFT JOIN TBS004 C (NOLOCK) ON A.PRPVENCOD = C.VENCOD
		
	WHERE 
	PRPSIT = 'P' AND
	PRPMOVEST = 'S' 
	
	
	--ORDER BY A.PROCOD, B.PDVDATCAD 
	
	UNION
			
SELECT
	'ND' collate Latin1_General_BIN AS 'ND',
	'SDC' collate Latin1_General_BIN AS 'SDC',
	E.CCSNOM collate Latin1_General_BIN  AS VEN,
	0 AS CLICOD,
	B.SDCNUM AS PED, 
	B.PROCOD collate Latin1_General_BIN AS COD,
	F.PRODES collate Latin1_General_BIN AS DESCRICAO,
	F.PROUM1 collate Latin1_General_BIN AS UNI1,
	(SDCQTDPED-SDCQTDATD-SDCQTDRES) * SDCQTDEMB AS QTD, 
	isnull(G.TDPPRELOJ1,0) * (SDCQTDPED-SDCQTDATD-SDCQTDRES) * SDCQTDEMB AS VAL,
	SDCDATCAD AS DATCAD,
	DATEDIFF(DAY,SDCDATCAD,getdate()) AS DIAS_PEN,
	CASE WHEN SDCPEN = 'S' THEN 'P' ELSE 'N' END collate Latin1_General_BIN AS SIT, 
	LESCOD AS ESTLOC,
	(SELECT SUM(ESTQTDCMP)FROM TBS032 J(NOLOCK) WHERE B.PROCOD = J.PROCOD AND ESTLOC IN(1,2) GROUP BY PROCOD) AS COMPRAS,
	(SELECT SUM(ESTQTDATU-ESTQTDRES)FROM TBS032 J(NOLOCK) WHERE B.PROCOD = J.PROCOD AND ESTLOC IN(1,2) GROUP BY PROCOD) AS DISPONIVEL,
	(SELECT SUM(ESTQTDATU-ESTQTDRES)FROM TBS032 J(NOLOCK) WHERE B.PROCOD = J.PROCOD AND ESTLOC IN(1) GROUP BY PROCOD) AS EST,
	(SELECT SUM(ESTQTDATU-ESTQTDRES)FROM TBS032 J(NOLOCK) WHERE B.PROCOD = J.PROCOD AND ESTLOC IN(2) GROUP BY PROCOD) AS LOJA	
			
	FROM  TBS0761 B (NOLOCK) 
	LEFT JOIN TBS076 C (NOLOCK) ON C.SDCNUM = B.SDCNUM
	LEFT JOIN TBS031 G (NOLOCK) on B.PROCOD = G.TDPPROCOD
	LEFT JOIN TBS010 F (NOLOCK) ON B.PROCOD = F.PROCOD
	LEFT JOIN TBS036 E (NOLOCK) ON C.CCSCOD = E.CCSCOD
			
	WHERE
	SDCPEN = 'S' AND 
	SDCQTDPED > (SDCQTDBAI +SDCQTDRES)
	
	UNION

