	PRPCLICOD AS CLICOD,
	PRPNUM AS PED , 
	PROCOD collate Latin1_General_BIN AS COD,
	PRODES collate Latin1_General_BIN AS DESCRICAO,
	PROUM1 collate Latin1_General_BIN AS UNI1,
	(PRPQTD*PRPQTDEMB) AS QTD ,
	(PRPPRELIQ/PRPQTDEMB)*(PRPQTD*PRPQTDEMB) as VAL,
	CASE WHEN SUBSTRING(PDVUSUGER,1,19) <> '' 
		THEN SUBSTRING(PDVUSUGER,1,19)
		ELSE PDVDATCAD END AS DATCAD ,
	DATEDIFF(DAY,PDVDATCAD,getdate()) AS DIAS_PEN,
	PRPSIT collate Latin1_General_BIN AS SIT,
	PRPESTLOC AS ESTLOC,
	COMPRAS,
	DISPONIVEL,
	EST,
	LOJA	
	
	FROM OPENQUERY(cd,'SELECT PRPCLICOD,PRPNUM,C.VENNOM,A.PROCOD,F.PRODES,F.PROUM1,PRPQTD,PRPQTDEMB,PRPPRELIQ,PDVUSUGER,PDVDATCAD,PRPSIT,PRPESTLOC, SUM(ESTQTDCMP) AS COMPRAS,
						SUM(ESTQTDATU-ESTQTDRES) AS DISPONIVEL,
						(SELECT SUM(ESTQTDATU-ESTQTDRES)FROM TBS032 J(NOLOCK) WHERE A.PROCOD = J.PROCOD AND ESTLOC IN(1) GROUP BY PROCOD) AS EST,
