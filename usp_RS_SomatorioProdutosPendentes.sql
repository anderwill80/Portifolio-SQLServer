create procedure [dbo].[usp_RS_SomatorioProdutosPendentes]
	@empcod smallint,
	@COD CHAR(15), 
	@DESCRI CHAR(60), 
	@OPCAO int 
as

begin

if object_id('TempDB.dbo.#saldocd') is not null
   drop table #saldocd
SELECT PROCOD AS CODIGO,SUM(ESTQTDATU-ESTQTDRES) AS DISPONIVEL
INTO #saldocd
FROM cd.SIBD.dbo.TBS032 A (nolock) WHERE ESTLOC IN (1) AND (ESTQTDATU <> 0)  GROUP BY PROCOD

if object_id('TempDB.dbo.#PEN') is not null
   drop table #PEN 


select 
A.PROSTATUS AS ST,
A.PROCOD AS COD,
A.PRODES AS DES,
A.PROUM1 AS UN1,
CASE WHEN ISNULL(C.DISPONIVEL,0) > 0 AND 
		 (ISNULL(SC.QTD,0)+ ISNULL(PV.QTD,0) - ISNULL(N.DISPONIVEL,0) - ISNULL(RCD.QTD,0) - ISNULL(TR.QTD,0)) < ISNULL(C.DISPONIVEL,0)
	THEN
	CASE WHEN ISNULL(SC.QTD,0)+ ISNULL(PV.QTD,0) - ISNULL(N.DISPONIVEL,0) - ISNULL(RCD.QTD,0) - ISNULL(TR.QTD,0) < 0 
		THEN 
		0
		ELSE
		ISNULL(SC.QTD,0)+ ISNULL(PV.QTD,0) - ISNULL(N.DISPONIVEL,0) - ISNULL(RCD.QTD,0) - ISNULL(TR.QTD,0)
		END
	ELSE
	ISNULL(C.DISPONIVEL,0)
	END	AS TRANSFERIR,
ISNULL(N.DISPONIVEL,0) AS SALDESTND,
ISNULL(N.LOJA,0) AS SALDLOJAND,
ISNULL(SC.QTD,0)+ ISNULL(PV.QTD,0) AS QTDPENND,
ISNULL(N.CMP,0) AS QTDCMPND,
ISNULL(C.DISPONIVEL,0) AS SALDOESTCD,
ISNULL(RCD.QTD,0) AS RESCD,
ISNULL(TR.QTD,0) AS TRCD 

INTO #PEN

FROM TBS010 A (NOLOCK) 
-- PENDENTE NA ND
LEFT JOIN 
	(select PROCOD AS COD , SUM(PRPQTD*PRPQTDEMB) AS QTD 
	FROM TBS058 A (NOLOCK) LEFT JOIN TBS055 B (NOLOCK) ON A.PRPNUM = B.PDVNUM
	WHERE PRPSIT = 'P' 
	GROUP BY PROCOD,PRPSIT) AS PV ON A.PROCOD = PV.COD

-- SDC DA ND
LEFT JOIN 
	(SELECT PROCOD AS COD,SUM((SDCQTDPED-SDCQTDATD)*SDCQTDEMB) AS QTD
	FROM TBS0761 (NOLOCK)
	WHERE SDCPEN = 'S'
	GROUP BY PROCOD) AS SC ON A.PROCOD = SC.COD 

-- DISPONIVEL NO CD	
LEFT JOIN 
	#saldocd AS C ON A.PROCOD COLLATE Latin1_General_CI_AS = C.CODIGO 
	
-- DISPONIVEL NA ND
LEFT JOIN 
	(SELECT PROCOD AS CODIGO,SUM(ESTQTDATU-ESTQTDRES) AS SALTT, 
	SUM(ESTQTDCMP) AS CMP,
	SUM(ESTQTDATU) AS ATU,
	ISNULL((SELECT SUM(ESTQTDATU-ESTQTDRES) FROM TBS032 B (nolock) WHERE ESTLOC =1 AND A.PROCOD = B.PROCOD GROUP BY PROCOD),0) AS DISPONIVEL,
	ISNULL((SELECT SUM(ESTQTDATU-ESTQTDRES) FROM TBS032 B (nolock)  WHERE ESTLOC =2 AND A.PROCOD = B.PROCOD GROUP BY PROCOD),0) AS LOJA	
	FROM TBS032 A (nolock) 
	WHERE ESTLOC IN (1,2) and (ESTQTDATU <> 0 OR ESTQTDPEN <> 0 OR ESTQTDCMP <>0) 
	GROUP BY PROCOD) AS N ON A.PROCOD = N.CODIGO

-- RESERVADO NO CD PARA ND	
LEFT JOIN
	(SELECT PROCOD ,SUM(PRPQTD*PRPQTDEMB) AS QTD
	FROM cd.SIBD.dbo.TBS058 
	WHERE PRPCLICOD IN (SELECT CLICOD FROM cd.SIBD.dbo.TBS002 WHERE CLICGC = '65069593000198') 
	GROUP BY PROCOD) AS RCD	ON A.PROCOD COLLATE Latin1_General_CI_AS = RCD.PROCOD


-- PRODUTOS EM TRASITO CD PARA ND
LEFT JOIN 
	(SELECT A.PROCOD AS COD, 
	ISNULL(sum(NFS.QTD),0) AS QTD

	FROM TBS010 AS A (NOLOCK)  
	LEFT JOIN
		(SELECT A.NFSNUM AS NFS,
		A.PROCOD COLLATE Latin1_General_CI_AS AS CÓD,
		A.NFSQTD * A.NFSQTDEMB AS QTD
		FROM  cd.SIBD.dbo.TBS0671 AS A (NOLOCK) 
		INNER JOIN cd.SIBD.dbo.TBS067 AS B (NOLOCK) ON A.NFSEMPCOD = B.NFSEMPCOD AND A.NFSNUM = B.NFSNUM AND A.SNEEMPCOD = B.SNEEMPCOD AND A.SNESER = B.SNESER
	
		INNER JOIN cd.SIBD.dbo.TBS080 AS D (NOLOCK) ON D.SNEEMPCOD = A.SNEEMPCOD 
		AND D.SNESER = A.SNESER AND D.SNEEMPCOD = B.SNEEMPCOD AND D.SNESER = B.SNESER AND B.NFSNUM = D.ENFNUM AND D.ENFNUM = A.NFSNUM
			   
		WHERE (D.ENFCNPJCPF = '65069593000198') AND (D.ENFSIT = '6') AND (B.NFSDATEMI BETWEEN GETDATE()-21 AND GETDATE()) 
					   --(B.NFSDATEMI BETWEEN @Data_de AND @Data_ate)
		GROUP BY A.NFSNUM, A.NFSQTD, A.NFSQTDEMB, A.PROCOD) AS NFS ON A.PROCOD = NFS.CÓD 

	LEFT JOIN
		(SELECT B.NFENUM AS NFE, 
		C.PROCOD COLLATE Latin1_General_CI_AS AS CÓD
		FROM TBS0591 AS C (NOLOCK) 
	
		LEFT JOIN TBS059 AS B (NOLOCK) ON B.NFECOD = C.NFECOD AND B.NFEEMPCOD = C.NFEEMPCOD 
		AND B.NFENUM = C.NFENUM AND B.NFETIP = C.NFETIP AND B.SERCOD = C.SERCOD AND B.SEREMPCOD = C.SEREMPCOD 
	
		LEFT JOIN TBS006 AS A (NOLOCK) ON A.FORNOM = B.NFENOM
	
		WHERE  (B.NFECAN = 'N') AND  NFENOM LIKE('%TANBY%') 
		AND((A.FORCGC = '65069593000350') OR A.FORNOM IS NULL)) AS NFE ON A.PROCOD = NFE.CÓD AND NFE.NFE = NFS.NFS
							  
	WHERE (NFE.NFE IS NULL)	
	GROUP BY A.PROCOD) AS TR ON A.PROCOD = TR.COD

WHERE ISNULL(SC.QTD,0)+ ISNULL(PV.QTD,0) <> 0
	  AND A.PROCOD LIKE(CASE WHEN @COD = '' THEN '% %' ELSE Upper(RTRIM(@COD))+'%' END)  
	  AND A.PRODES LIKE(CASE WHEN @DESCRI = '' THEN '% %' ELSE '%'+LTRIM(Upper(RTRIM(@DESCRI)))+'%' END)


IF @OPCAO = 1  -- TODOS
	SELECT * FROM #PEN
	
IF @OPCAO = 2 -- TEM NO ESTOQUE MAS ESTA PENDENTE
	SELECT * FROM #PEN
	WHERE SALDESTND > 0

IF @OPCAO = 3 -- TEM NO ESTOQUE MAIOR QUE PENDENTE
	SELECT * FROM #PEN
	WHERE SALDESTND > QTDPENND
	
IF @OPCAO = 4 -- TEM NO CD MAS ESTA PENDENTE
	SELECT * FROM #PEN
	WHERE SALDOESTCD > 0 
	
IF @OPCAO = 5 -- TEM NO CD MAIOR QUE PENDENTE
	SELECT * FROM #PEN
	WHERE SALDOESTCD > QTDPENND  
	
IF @OPCAO = 6 -- TRANSFERIR PARA ND
	SELECT * FROM #PEN
	WHERE TRANSFERIR > 0

IF @OPCAO = 7 -- PRODUTOS EM TRANSITO
	SELECT * FROM #PEN
	WHERE TRCD > 0


SELECT 
ST,
COD,
DES,
UN1,
TRANSFERIR,
SALDESTND,
SALDLOJAND,
QTDPENND,
QTDCMPND,
SALDOESTCD,
RESCD,
TRCD 

FROM #PEN

END

