create proc [dbo].[usp_RS_Estoques]
as 
begin
	SELECT 
	LESCOD AS LESCOD, 
	RTRIM(LTRIM(STR(LESCOD))) + ' - ' + RTRIM(LTRIM(LESDES)) AS LESDES  

	FROM TBS034 (nolock) 
	ORDER BY LESCOD
End
