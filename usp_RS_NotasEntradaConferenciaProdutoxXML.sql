				END
		END AS NFEBASICMS, 

		A.NFEREDBASICMS,
		-- (A.NFEQTD * A.NFEPRE) - A.NFEVALDESITE + A.NFEVALFREITE + A.NFEVALOUTDES + A.NFEVALSEGITE - A.NFEBASICMS as VALRED,

		CASE WHEN (A.NFEVALICMSST > 0 AND B.NFETIP NOT IN ('D')) OR SUBSTRING(A.NFECFOP,3,3) IN ('556','557') OR (A.LESCOD = 6 AND SUBSTRING(A.NFECFOP,3,3) IN ('910'))
			THEN 0
			ELSE 
				CASE WHEN NFEPCRESN > 0 
					THEN NFEPCRESN
					ELSE NFEPERICMS
				END
		END AS NFEPERICMS,

		CASE WHEN (A.NFEVALICMSST > 0 AND B.NFETIP NOT IN ('D')) OR SUBSTRING(A.NFECFOP,3,3) IN ('556','557') OR (A.LESCOD = 6 AND SUBSTRING(A.NFECFOP,3,3) IN ('910'))
			THEN 0
			ELSE 
				CASE WHEN NFEVCRESN > 0 
					THEN NFEVCRESN
					ELSE round(A.NFEVALICMS,2)
				END
		END AS NFEVALICMS,
		C.itemXml,
		CASE WHEN A.NFEQTD <> 0 
			THEN CONVERT(DECIMAL(10,4),ROUND(A.NFETOTOPEITE / (A.NFEQTD * CASE WHEN A.NFEQTDEMB = 0 THEN 1 ELSE A.NFEQTDEMB END),4)) 
			ELSE 0
		END AS NFEPREUNI
	FROM TBS0591 A (NOLOCK)
		INNER JOIN TBS059 B (NOLOCK) ON A.NFEEMPCOD = B.NFEEMPCOD AND A.NFECOD = B.NFECOD AND A.NFENUM = B.NFENUM AND A.NFETIP = B.NFETIP AND A.SERCOD = B.SERCOD AND A.SEREMPCOD = B.SEREMPCOD
		INNER JOIN #NFEXML C ON A.NFEEMPCOD = C.NFEEMPCOD AND A.NFECOD = C.NFECOD AND A.NFENUM = C.NFENUM AND A.NFETIP = C.NFETIP AND A.SERCOD = C.SERCOD AND A.SEREMPCOD = C.SEREMPCOD AND A.NFEITE = C.NFEITEXML
		LEFT JOIN #NFECFOP D (NOLOCK) ON SUBSTRING(A.NFECFOP,3,3) COLLATE DATABASE_DEFAULT = D.NFECFOP
		LEFT JOIN #TBS006 E (NOLOCK) ON B.NFECOD = E.FORCOD AND A.NFETIP NOT IN ('D')
		LEFT JOIN #TBS002 F (NOLOCK) ON B.NFECOD = F.CLICOD AND A.NFETIP IN ('D')

	-- select * from #XMLSIS WHERE NFENUM = 19036

------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	-- DELETAR TUDO O QUE NĂO TEM XML DA LINHA DO XML

	DELETE #XMLSIS
	WHERE
		SIS = 'XML' AND
		NFETIP = 'D'
------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	-- UPDATE NOS ITENS QUE FORAM RATEADOS 

	BEGIN TRAN 
		UPDATE #XMLSIS SET 
		NFEQTD = CASE WHEN NFECALC = 'M' THEN NFEQTDXML * NFEFATOR ELSE CASE WHEN NFECALC = 'D' THEN NFEQTDXML / NFEFATOR ELSE NFEQTD END END 

		FROM #XMLSIS A 
		INNER JOIN (
				SELECT 
				A.NFEEMPCOD,
				A.NFECOD,
				A.NFENUM,
				A.NFETIP,
				A.SERCOD,
				A.SEREMPCOD,
				A.SIS,
				A.NFEPROFOR
		
				FROM #XMLSIS A
		
				WHERE 
				itemXml > 1 AND SIS = 'XML' 
		
				GROUP BY
				A.NFEEMPCOD,
				A.NFECOD,
				A.NFENUM,
				A.NFETIP,
				A.SERCOD,
				A.SEREMPCOD,
				A.SIS, 
				A.NFEPROFOR		) B ON A.NFEEMPCOD = B.NFEEMPCOD AND A.NFECOD = B.NFECOD AND A.NFENUM = B.NFENUM AND A.NFETIP = B.NFETIP AND A.SERCOD = B.SERCOD AND A.SEREMPCOD = B.SEREMPCOD AND A.SIS = B.SIS AND A.NFEPROFOR = B.NFEPROFOR

		WHERE 
		A.itemXml = 1

		-- DELETAR ITENS XML QUE FORAM RATEADOS
		DELETE #XMLSIS 
		WHERE 
			itemXml > 1 AND
			SIS = 'XML'
	COMMIT TRAN 

------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	-- CRIAR A TABELA DE ITENS 

	IF object_id('tempdb.dbo.#ITENS') IS NOT NULL
	   DROP TABLE #ITENS;

	SELECT 
		SIS,
		OP,
		NFETIPDES,
		NFESUBTIP,
		NFECOD,
		CLIENTEFORNECEDOR,
		NFENUM,
		NFEDATEMI,
		NFEDATEFE,
		NFEESTORI,
		NFENOSFOR,
		LESCOD,
		NFEITEXML ,
		NFEPROFOR,
		NFEDESXML,
		NFEUMXML,
		NFEQTDXML,
		NFECALC,
		NFEFATOR,
		NFEQTD,
		NFEMOVEST,
		NFECFOPXML,
		NFECFOPDES,
		NFEPREXML,
		NFEVALPROXML,
		NFEVALDESITEXML,
		NFEVALFREITEXML,
		NFEVALSEGITEXML,
		NFEVALOUTITEXML,
		NFEBASIPIXML,
		NFEPERIPIXML,
		NFEVALIPIXML,
		NFEBASICMSSTXML,
		NFEPERICMSSTXML,
		NFEVALICMSSTXML,
		NFETOTOPEITEXML,
		NFENCMXML,
		NFECSTXML,
		NFEBASICMSXML,
		NFEREDBASICMS,
		-- (A.NFEQTDXML * A.NFEPREXML) - A.NFEVALDESITEXML + A.NFEVALFREITEXML + A.NFEVALOUTITEXML + A.NFEVALSEGITEXML - A.NFEBASICMSXML as VALRED,
		NFEPERICMSXML,
		NFEVALICMSXML,
		isnull(B.TDPPRECOR1,0) AS precoVenda,
		isnull(B.TDPCUSBAS,0) AS precoCusto,
		NFEPREUNI
	INTO #ITENS	FROM #XMLSIS A
		LEFT JOIN TBS031 B (NOLOCK) ON 
		A.NFEPROFOR = B.TDPPROCOD
	ORDER BY
		NFEDATEFE,
		NFECOD, 
		NFENUM,
		NFETIPDES,
		NFESUBTIP,
		NFEITEXML,
		SIS DESC 

------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------
	-- CRIAR A TABELA POR NOTA 

	IF object_id('tempdb.dbo.#NOTA') IS NOT NULL
	   DROP TABLE #NOTA;

	SELECT 
		SIS,
		OP,
		NFETIPDES,
		NFESUBTIP,
		NFECOD,
		CLIENTEFORNECEDOR,
		NFENUM,
		NFEDATEMI,
		NFEDATEFE,
		NFEESTORI,
		NFENOSFOR,
		'' AS LESCOD,
		-1 AS NFEITEXML ,
		'' AS NFEPROFOR,
		'' AS NFEDESXML,
		'' AS NFEUMXML,
		0 AS NFEQTDXML,
		'' AS NFECALC,
		0 AS NFEFATOR,
		0 AS NFEQTD,
		'' AS NFEMOVEST,
		'' AS NFECFOPXML,
		'' AS NFECFOPDES,
		0 AS NFEPREXML,
		ROUND(SUM(NFEVALPROXML),2) 		AS NFEVALPROXML,
		ROUND(SUM(NFEVALDESITEXML),2) 	AS NFEVALDESITEXML,
		ROUND(SUM(NFEVALFREITEXML),2) 	AS NFEVALFREITEXML,
		ROUND(SUM(NFEVALSEGITEXML),2) 	AS NFEVALSEGITEXML,
		ROUND(SUM(NFEVALOUTITEXML),2) 	AS NFEVALOUTITEXML,
		ROUND(SUM(NFEBASIPIXML),2)		AS NFEBASIPIXML,

		CASE WHEN ROUND(SUM(NFEBASIPIXML),2) = 0
			THEN 0
			ELSE ROUND(SUM(NFEVALIPIXML),2) / ROUND(SUM(NFEBASIPIXML),2) * 100
		END  AS NFEPERIPIXML,

		ROUND(SUM(NFEVALIPIXML),2)		AS NFEVALIPIXML,
		ROUND(SUM(NFEBASICMSSTXML),2)	AS NFEBASICMSSTXML,

		CASE WHEN ROUND(SUM(NFEBASICMSSTXML),2) = 0
			THEN 0
			ELSE ROUND(SUM(NFEVALICMSSTXML),2) / ROUND(SUM(NFEBASICMSSTXML),2) * 100
		END  AS NFEPERICMSSTXML,

		ROUND(SUM(NFEVALICMSSTXML),2)	AS NFEVALICMSSTXML,
		ROUND(SUM(NFETOTOPEITEXML),2)	AS NFETOTOPEITEXML,
		'' AS NFENCMXML,
		'' AS NFECSTXML,
		ROUND(SUM(NFEBASICMSXML),2)		AS NFEBASICMSXML,
		0 AS NFEREDBASICMS,
		-- (A.NFEQTDXML * A.NFEPREXML) - A.NFEVALDESITEXML + A.NFEVALFREITEXML + A.NFEVALOUTITEXML + A.NFEVALSEGITEXML - A.NFEBASICMSXML as VALRED,
		0 AS NFEPERICMSXML,
		ROUND(SUM(NFEVALICMSXML),2)		AS NFEVALICMSXML,
		0 AS precoVenda,
		0 AS precoCusto,
		0 AS NFEPREUNI
	INTO #NOTA FROM #XMLSIS 

	GROUP BY 
		SIS,
		OP,
		NFETIPDES,
		NFESUBTIP,
		NFECOD,
		CLIENTEFORNECEDOR,
		NFENUM,
		NFEDATEMI,
		NFEDATEFE,
		NFEESTORI,
		NFENOSFOR
	ORDER BY 
		NFEDATEFE,
		NFECOD,
		NFENUM,
		NFETIPDES,
		NFESUBTIP,
		NFEITEXML, 
		SIS DESC 

	-- DELETAR TUDO O QUE NĂO TEM XML DA LINHA DO XML
	DELETE #ITENS
	WHERE 
		NFEDESXML = '' AND
		NFEUMXML = '' AND
		NFEITEXML >= 0 

------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------	
	-- Tabela final

	SET @QuantidadeNotas = ( select sum(a.qtd) from ( select 1 AS qtd from #NOTA WHERE SIS = 'SIS' GROUP BY OP, NFETIPDES, NFECOD, NFENUM ) as a )
	SET @ValorTotalNotas = (select sum(NFETOTOPEITEXML) from #NOTA WHERE SIS = 'SIS')
	
	SELECT 
		@QuantidadeNotas as QuantidadeNotas, 
		@ValorTotalNotas as ValorTotalNotas, 
		* 
	FROM #NOTA 

	UNION 
	SELECT 
		@QuantidadeNotas as QuantidadeNotas,
		@ValorTotalNotas as ValorTotalNotas, 
		*
	FROM #ITENS
END