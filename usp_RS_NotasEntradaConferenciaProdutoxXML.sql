		NFEDATEMI BETWEEN @DataEmissao_De AND @DataEmissao_Ate AND
		NFEDATENT BETWEEN @DataEntrada_De AND @DataEntrada_Ate AND
		NFEDATEFE BETWEEN @Date_De AND @Date_Ate AND		
		A.NFETIP IN (SELECT valor FROM #TIPOSNOTA) AND 
		A.NFECOD = CASE WHEN @NFECOD = 0 THEN A.NFECOD ELSE @NFECOD END AND		
		A.NFENUM = CASE WHEN @NFENUM = 0 THEN A.NFENUM ELSE @NFENUM END AND
		A.PROCOD COLLATE DATABASE_DEFAULT IN (SELECT PROCOD FROM #CodigosProduto) AND 
		A.NFEPROFOR = CASE WHEN @CodigoProduto_XML = '' THEN A.NFEPROFOR ELSE @CodigoProduto_XML END AND 
		A.NFEDESXML LIKE(CASE WHEN @Descricao_XML = '' THEN '% %' ELSE @Descricao_XML END) AND 		
		A.NFEMOVEST IN (SELECT valor FROM #MOVEST) AND
		B.NFECAN IN (SELECT valor FROM #CANCEL) AND 
		B.NFENOM LIKE(CASE WHEN @NFENOM = '' THEN B.NFENOM ELSE @NFENOM END ) AND		
		LEN(B.NFEUSUEFE) BETWEEN (CASE WHEN @SeEfetivado = 'T' OR @SeEfetivado = 'N' THEN 0 ELSE 1 END) AND (CASE WHEN @SeEfetivado = 'N' THEN 0 ELSE 50 END) AND
		(D.ENFTIPDOC IS NOT NULL OR (NFENOSFOR <> 'S')) --/*AND B.NFECAN <> 'S' AND NFEDATEFE <> ''*/)) -- TENHO QUE TIRAR ESSE FILTRO QUE ESTÁ ENTRE /* */ PARA QUE EU POSSA FILTRAR AS NOTAS ESTĂO CANCELADAS E EM ABERTO.		
		-- para filtrar tudo preciso comentar movimenta estoque, nota cancelada, efetivaçăo e tipo de nota
	GROUP BY 
		B.NFEEMPCOD,
		B.NFETIP,
		B.SEREMPCOD,
		B.SERCOD,
		NFECAN,
		NFEUSUEFE,
		B.NFESUBTIP,
		B.NFECOD,
		ISNULL(CLINOM, FORNOM),
		B.NFENUM,
		B.NFEDATEMI,
		B.NFEDATEFE,
		B.NFEESTORI,
		B.NFENOSFOR,
		A.NFEITEXML,
		A.NFEPROFOR,
		A.NFEDESXML,
		A.NFEUMXML,
		A.NFEQTDXML,
		A.NFECALC,
		A.NFEFATOR,
		A.NFECFOPXML,
		G.NFECFOPDES,
		A.NFEPREXML,
		A.NFEVALPROXML,
		A.NFEVALDESITEXML,
		A.NFEVALFREITEXML,
		A.NFEVALSEGITEXML,
		A.NFEVALOUTITEXML,
		A.NFEBASIPIXML,
		A.NFEPERIPIXML,
		A.NFEVALIPIXML,
		A.NFEBASICMSSTXML,
		A.NFEPERICMSSTXML,
		A.NFEVALICMSSTXML,
		A.NFENCMXML,
		A.NFECSTXML,
		A.NFEBASICMSXML,
		A.NFEREDBASICMS,
		A.NFEPERICMSXML,
		A.NFEVALICMSXML,
		A.NFEITE,
		A.NFEPCRESN,
		A.NFEVCRESN,
		A.NFEVCRESNXML
	ORDER BY 
		B.NFEDATEFE,
		B.NFENUM,
		A.NFEITEXML

