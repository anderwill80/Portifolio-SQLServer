		LEFT JOIN #TBS080 D (NOLOCK) ON A.NFENUM = D.ENFNUM AND A.NFECOD = D.ENFCODDES
		LEFT JOIN #TBS006 E (NOLOCK) ON B.NFECOD = E.FORCOD AND B.NFETIP NOT IN ('D')
		LEFT JOIN #TBS002 F (NOLOCK) ON B.NFECOD = F.CLICOD AND B.NFETIP IN ('D')
		LEFT JOIN #NFECFOPXML G 	 ON SUBSTRING(A.NFECFOPXML,3,3) COLLATE DATABASE_DEFAULT = G.NFECFOPXML
